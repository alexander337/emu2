DROP TABLE IF EXISTS `characters`;
CREATE TABLE  `characters` (
	`ID` INTEGER auto_increment,
	`AccountID` INTEGER NOT NULL,
	`refCharID` INTEGER NOT NULL,
	`Name` varchar(12) NOT NULL,
	`Volume` INTEGER(1) NOT NULL,
	`Level` INTEGER(1) NOT NULL DEFAULT 1,
	`MaxLevel` INTEGER(1) NOT NULL DEFAULT 1,
	`Strength` INTEGER NOT NULL DEFAULT 20,
	`Intelligence` INTEGER NOT NULL DEFAULT 20,
	`HP` INTEGER NOT NULL DEFAULT 200,
	`MP` INTEGER NOT NULL DEFAULT 200,
	`Stats` INTEGER NOT NULL DEFAULT 0,
	`SP` INTEGER NOT NULL DEFAULT 0,
	`Exp` INTEGER NOT NULL DEFAULT 0,
	`SPExp` INTEGER NOT NULL DEFAULT 0,
	`Gold` INTEGER NOT NULL DEFAULT 0,
	`GoldStored` INTEGER NOT NULL DEFAULT 0,
	`BerserkCount` INTEGER(1) NOT NULL DEFAULT 0,
	`PK` INTEGER NOT NULL DEFAULT 0,
	`PKLvl` INTEGER NOT NULL DEFAULT 0,
	`Zone` INTEGER NOT NULL,
	`X` float NOT NULL,
	`Y` float NOT NULL,
	`Z` float NOT NULL,
	`Angle` INTEGER NOT NULL,
	`GM` INTEGER(1) NOT NULL,
	`Rank` INTEGER(1) NOT NULL DEFAULT 0,
	`InventorySlots` INTEGER(2) NOT NULL DEFAULT 45,
	`InventorySlotsInc` BOOLEAN NOT NULL DEFAULT FALSE,
	`StorageSlots` INTEGER(3) NOT NULL DEFAULT 149,
	`StorageSlotsInc` BOOLEAN NOT NULL DEFAULT FALSE,
	`GuildID` INTEGER NOT NULL DEFAULT 0,
	`AcademyID` INTEGER NOT NULL DEFAULT 0,
	`ReturnID` INTEGER NOT NULL,
	`StallAvatarID` INTEGER NOT NULL DEFAULT 0,
	`AbilityPetID` INTEGER NOT NULL DEFAULT 0,
	`AttackPetID` INTEGER NOT NULL DEFAULT 0,
	`TransportID` INTEGER NOT NULL DEFAULT 0,
	`AutopotHPActive` INTEGER(1) NOT NULL DEFAULT 0,
	`AutopotHPValue` INTEGER(3) NOT NULL DEFAULT 0,
	`AutopotHPBar` INTEGER(1) NOT NULL DEFAULT 0,
	`AutopotHPSlot` INTEGER(1) NOT NULL DEFAULT 0,
	`AutopotMPActive` INTEGER(1) NOT NULL DEFAULT 0,
	`AutopotMPValue` INTEGER(3) NOT NULL DEFAULT 0,
	`AutopotMPBar` INTEGER(1) NOT NULL DEFAULT 0,
	`AutopotMPSlot` INTEGER(1) NOT NULL DEFAULT 0,
	`AutopotPillActive` INTEGER(1) NOT NULL DEFAULT 0,
	`AutopotPillBar` INTEGER(1) NOT NULL DEFAULT 0,
	`AutopotPillSlot` INTEGER(1) NOT NULL DEFAULT 0,
	`AutopotDelayActive` INTEGER(1) NOT NULL DEFAULT 0,
	`AutopotDelay` INTEGER(3) NOT NULL DEFAULT 0,
	`State` INTEGER(1) NOT NULL DEFAULT 0,
	`ExpirationMark` INTEGER(1) NOT NULL DEFAULT 0,
	`ExpirationDate` datetime NOT NULL,
	`CreationDate` datetime NOT NULL,
	PRIMARY KEY  (`ID`),
	UNIQUE KEY (`Name`)
);
